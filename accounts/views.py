@login_required
def coupon_form(request):
    guider = CouponCode.objects.all()

    if request.method == 'POST':
        form = CouponCodeForm(request.POST)
        if form.is_valid():
            coupon = form.save(commit=False)
            coupon.user = request.user
            if coupon.coupon_code in codes:
                for each in guider :
                    if coupon.guider == f'{each}' and coupon.guider != f'{request.user}':
                        coupon.is_activated = True
                        coupon.save()
                        messages.success(request, 'Congratulations, Your account has been activated')
                        return redirect('profile')
                else:
                    messages.error(request, 'OOps, no such user. Guider may be case-sensitive')
            else:
                messages.error(request, 'Invalid Coupon Code')
    else:
        form = CouponCodeForm()
    return render(request, 'coupon_form.html', {'form': form})


@login_required
def profile(request):
    coupon = CouponCode.objects.filter(guider=request.user).count()*500
    profile = Profile.objects.all()
    earning = Earning.objects.get(user=request.user)
    post = Post.objects.filter(created_by=request.user, approve=True).count()*50
    earned, boolCreated = Earning.objects.update_or_create(user=request.user, defaults={'approved_post': post,
                                                                                        'guider_bonus': coupon}, )
    earning.approved_post = post
    earning.guider_bonus = coupon
    earning.activities_earning = F('daily_login') + F('approved_post') + F('news_earned')
    earning.activities_earning_remain = F('daily_login') + F('approved_post') + F('news_earned')\
                                        - F('activities_earning_withdrawn')
    earning.guider_bonus_remain = F('guider_bonus') - F('guider_bonus_withdrawn')
    earning.save()
    earning = Earning.objects.get(user=request.user)
    return render(request, 'profile.html', {'coupon': coupon, 'profile': profile, 'earning': earning, 'post': post})

@login_required
def withdrawal_form(request):
    earning = Earning.objects.get(user=request.user)
    profile = Profile.objects.all()
    withdrawal = Withdrawal.objects.all()
    coupon = CouponCode.objects.all()
    week = datetime.now().weekday()
    time = datetime.now().hour

    if request.method == 'POST':
        form = WithdrawalForm(request.POST)
        if form.is_valid():
            withdrawal = form.save(commit=False)
            if request.user.profile:
                withdrawal.user = request.user
                withdrawal.account_details = f'''bank name: {request.user.profile.bank_name}
                                                account_number: {request.user.profile.account_number}
                                                account name: {request.user.profile.account_name}'''
                withdrawal.facebook_link = request.user.profile.facebook_link
                if earning.activities_earning_remain >= withdrawal.activities_earning_to_withdraw >= 1500 or withdrawal.activities_earning_to_withdraw == 0:
                    if earning.guider_bonus_remain >= withdrawal.guider_bonus_to_withdraw >= 1000 or withdrawal.guider_bonus_to_withdraw == 0:
                        if withdrawal.activities_earning_to_withdraw != 0 or withdrawal.guider_bonus_to_withdraw != 0:
                            status, statusCreate = PaymentStatus.objects.update_or_create(user=request.user,
                            defaults={
                                'earnings_review': None,
                                'sponsored_post_verification': None,
                                'details_cross_check': None,
                                'last_payment_upload': None,
                                'deduction': None,
                                 'date': datetime.now(),
                                 })
                            payment = PaymentStatus.objects.get(user=request.user)
                            payment.made += 1
                            payment.save()
                            if request.user.coupon:
                                if withdrawal.activities_earning_to_withdraw % 1500 == 0:
                                    if withdrawal.guider_bonus_to_withdraw % 1000 == 0:

                                        withdrawal.save()
                                        messages.success(request, 'Withdrawal Successful!, You can start tracking your payment in your payment status')
                                        return redirect('payment_status')
                                    else:
                                        messages.error(request, 'Your guider bonus can only be withdrawn in multiple of 1000')
                                else:
                                    messages.error(request,
                                                   'Your activities earning can only be withdrawn in multiple of 1500')
                            else:
                                messages.error(request, 'Please Activate Your Account First')
                        else:
                            messages.error(request,
                                           "Both fields can't be 0. If you ain't withdrawing, please Kindly leave this Page")
                    else:
                        messages.error(request,
                                       "You can't withdraw more than your guider bonus and less than 1000 naira from your guider bonus")
                else:
                    messages.error(request,
                                   "You can't withdraw more than your activities earning and less than 1500 naira from your activities earnings")
            else:
                messages.error(request,
                  "You have to create a profile first")

    else:
        form = WithdrawalForm()
    return render(request, 'withdrawal_form.html', {'form': form,
                                                    'profile': profile,
                                                    'earning': earning,
                                                    'week': week,
                                                    'time': time})


